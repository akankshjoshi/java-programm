class operator{
    public static void main(String[] args){
        int number1 = 22;
        int number2 = 44;
        System.out.println(number1 + number2);
        System.out.println(number1 - number2);
        System.out.println(number1 * number2);
        System.out.println(number1/number2);
        System.out.println(number1 % number2);
    }
}